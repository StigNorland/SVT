class BlowUp(RuntimeError):
    pass


def evolve(med, psi, n_steps, dt, record_every=0, recorder=None):
    out = []
    for i in range(n_steps):
        psi = med.step(psi, dt)
        if (i + 1) % 200 == 0 and not np.isfinite(psi.flat[0]):
            raise BlowUp(f"non-finite field at step {i + 1}")
        if record_every and (i + 1) % record_every == 0 and recorder:
            out.append(recorder(psi, (i + 1) * dt))
    return psi, out


def echo_run(med, psi0, n_steps, dt, eps=0.0, seed=137):
    """Forward n_steps, conjugate (T), optional perturbation
    psi -> psi(1+eps*eta), forward n_steps, conjugate; return the echo
    field (directly comparable to psi0)."""
    psi, _ = evolve(med, psi0, n_steps, dt)
    psi = np.conj(psi)
    if eps:
        rng = np.random.default_rng(seed)
        eta = (rng.standard_normal(psi.shape)
               + 1j * rng.standard_normal(psi.shape)) / math.sqrt(2)
        psi = psi * (1.0 + eps * eta)
    psi, _ = evolve(med, psi, n_steps, dt)
    return np.conj(psi)


def find_annihilation(med, psi0, dt, t_max, check_every=20):
    """First time the defect count drops to zero (and stays there for
    one check window). Returns (t_annih or None, count timeline)."""
    psi = psi0.copy()
    n_steps = int(round(t_max / dt))
    timeline = []
    t_hit = None
    for i in range(n_steps):
        psi = med.step(psi, dt)
        if (i + 1) % check_every == 0:
            t = (i + 1) * dt
            nd = len(defects_2d(psi, med.dx))
            timeline.append((t, nd))
            if nd == 0 and t_hit is None:
                t_hit = t
            if nd > 0 and t_hit is not None and t - t_hit < 2 * check_every * dt:
                t_hit = None        # transient zero, keep looking
            if t_hit is not None and t > t_hit + 10:
                break
    return t_hit, timeline


