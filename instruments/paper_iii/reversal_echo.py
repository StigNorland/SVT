def winding_map(psi):
    """Plaquette winding numbers of a 2D field (periodic)."""
    th = np.angle(psi)

    def dwrap(a):
        return (a + np.pi) % (2 * np.pi) - np.pi

    dx_ = dwrap(np.roll(th, -1, 0) - th)
    dy_ = dwrap(np.roll(th, -1, 1) - th)
    circ = dx_ + np.roll(dy_, -1, 0) - np.roll(dx_, -1, 1) - dy_
    return np.rint(circ / (2 * np.pi)).astype(int)


def defects_2d(psi, dx):
    """List of (x, y, charge) for plaquette windings of a 2D field."""
    w = winding_map(psi)
    ii, jj = np.nonzero(w)
    n0, n1 = psi.shape
    return [((i - n0 / 2 + 0.5) * dx, (j - n1 / 2 + 0.5) * dx, int(w[i, j]))
            for i, j in zip(ii, jj)]


def defects_match(d_ref, d_got, tol):
    """Same count, and every reference defect has a same-charge partner
    within tol (greedy nearest matching)."""
    if len(d_ref) != len(d_got):
        return False
    got = list(d_got)
    for x, y, q in d_ref:
        best, bi = None, None
        for i, (x2, y2, q2) in enumerate(got):
            if q2 != q:
                continue
            r = math.hypot(x - x2, y - y2)
            if best is None or r < best:
                best, bi = r, i
        if best is None or best > tol:
            return False
        got.pop(bi)
    return True


def fidelity(a, b):
    num = abs(np.vdot(a, b))**2
    den = float(np.sum(np.abs(a)**2)) * float(np.sum(np.abs(b)**2))
    return float(num / den)


