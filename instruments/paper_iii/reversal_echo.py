    ax.set_ylabel("F")
    ax.set_title("recovery requires exact boundary data\n"
                 "(SSV-III sec. 8, measured)", fontsize=9)
    ax.legend(fontsize=8)
    fig.tight_layout()
    fig.savefig(dest, dpi=160)
    plt.close(fig)
    return dest


