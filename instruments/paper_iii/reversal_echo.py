B0 = 1.0                      # log coupling: c_s = sqrt(B0) = 1
XI = 1.0 / math.sqrt(2 * B0)  # healing length in these units
# Log regularization floor at vortex-core zeros. The LogSE term is
# regular there (|psi|^2 ln|psi|^2 -> 0), but a raw floor of ~1e-300
# lets grid points adjacent to a core zero acquire phase rotations of
# b*ln(rho)*dt/2 ~ -690*dt -- spectral pollution that destabilizes the
# split-step (the same amplification that destabilized the #119 H8c
# fine-grid relaxer). Flooring at 1e-6 caps the rotation at a fraction
# of a radian while leaving every point with rho > 1e-6 untouched; the
# substep stays exactly unitary and exactly T-invertible, so the echo
# protocol is unaffected.
RHO_FLOOR = 1e-6


