class Medium:
    """Split-step LogSE on a periodic grid (2D or 3D), dealiased.

    Optional chiral-shear advection (2D only): the #138-derived EOM term
    dpsi/dt -= G.grad psi, G = (lam/rho0^2)(dy w, -dx w), w = curl j,
    integrated as an RK2 substep."""

    def __init__(self, shape, dx, lam=0.0):
        self.shape, self.dx, self.lam = tuple(shape), dx, lam
        self.ndim = len(shape)
        ks = [2 * np.pi * np.fft.fftfreq(n, dx) for n in shape]
        KK = np.meshgrid(*ks, indexing="ij")
        self.K = KK
        self.k2 = sum(k**2 for k in KK)
        mask = np.ones(self.shape, bool)
        for k, kv in zip(KK, ks):
            mask &= np.abs(k) <= (2 / 3) * np.abs(kv).max()
        self.dealias = mask.astype(float)

    def _d(self, f, axis):
        return np.fft.ifftn(1j * self.K[axis] * self.dealias
                            * np.fft.fftn(f))

    def grad(self, f):
        return [self._d(f, a) for a in range(self.ndim)]

    def current(self, psi):
        return [(np.conj(psi) * g).imag for g in self.grad(psi)]

    def curl_j_2d(self, psi):
        jx, jy = self.current(psi)
        return (self._d(jy, 0) - self._d(jx, 1)).real

    def energy(self, psi):
        """E = int 1/2|grad|^2 + U(rho) + (lam/2)|curl j|^2, vacuum-zeroed."""
        rho = np.abs(psi)**2
        e_kin = 0.5 * sum(float(np.sum(np.abs(g)**2)) for g in self.grad(psi))
        with np.errstate(divide="ignore", invalid="ignore"):
            u = B0 * (rho * np.where(rho > 0, np.log(np.maximum(rho, RHO_FLOOR)),
                                     0.0) - rho + 1.0)
        e_pot = float(np.sum(u))
        e_chi = 0.0
        if self.lam and self.ndim == 2:
            w = self.curl_j_2d(psi)
            e_chi = 0.5 * self.lam * float(np.sum(w**2))
        return (e_kin + e_pot + e_chi) * self.dx**self.ndim

    def norm(self, psi):
        return float(np.sum(np.abs(psi)**2)) * self.dx**self.ndim

    def _chiral_rhs(self, psi):
        w = self.curl_j_2d(psi)
        gx = self.lam * self._d(w, 1).real
        gy = -self.lam * self._d(w, 0).real
        g = self.grad(psi)
        return -(gx * g[0] + gy * g[1])

    def step(self, psi, dt):
        rho = np.abs(psi)**2
        psi = psi * np.exp(-1j * B0 * np.log(np.maximum(rho, RHO_FLOOR))
                           * (dt / 2))
        psi = np.fft.ifftn(np.fft.fftn(psi)
                           * (np.exp(-1j * self.k2 * (dt / 2))
                              * self.dealias))
        if self.lam and self.ndim == 2:
            f1 = self._chiral_rhs(psi)
            f2 = self._chiral_rhs(psi + 0.5 * dt * f1)
            psi = psi + dt * f2
        rho = np.abs(psi)**2
        return psi * np.exp(-1j * B0 * np.log(np.maximum(rho, RHO_FLOOR))
                            * (dt / 2))

    def relax(self, psi, n_iter, dtau):
        """Imaginary-time preconditioner (#119 H7a lesson): the LogSE
        vacuum at rho0 = 1 has exactly zero energy, so imprints relax
        with no renormalisation and protected winding."""
        kin = np.exp(-self.k2 * (dtau / 2)) * self.dealias
        for _ in range(n_iter):
            rho = np.abs(psi)**2
            psi = psi * np.exp(-B0 * np.log(np.maximum(rho, RHO_FLOOR))
                               * (dtau / 2))
            psi = np.fft.ifftn(np.fft.fftn(psi) * kin)
            rho = np.abs(psi)**2
            psi = psi * np.exp(-B0 * np.log(np.maximum(rho, RHO_FLOOR))
                               * (dtau / 2))
        return psi


