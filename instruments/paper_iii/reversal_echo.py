def colliding_dipoles_2d(n, dx, d, s):
    """Two tight vortex-antivortex dipoles at x = -/+ s, opposite
    orientation, on a head-on collision course (annihilation completes
    on impact). GEOMETRY NOTE, recorded per rule 1: the pre-registration
    wrote 'vortex-antivortex pair'; a single isolated pair is a
    traveling solitary object that does not annihilate in free flight
    (and a pair tight enough to annihilate spontaneously is destroyed
    by the imaginary-time preconditioner, since v-av winding is not
    topologically protected). The event class and all decision rules
    are unchanged: defect count -> 0 with a radiated sound burst.
    Quadrupole-symmetric configuration also keeps the periodic seam
    clean (#119 H7a lesson)."""
    x = (np.arange(n) - n / 2) * dx
    X, Y = np.meshgrid(x, x, indexing="ij")

    def pair(cx, sign):
        Zp = (X - cx) + 1j * (Y - d / 2)
        Zm = (X - cx) + 1j * (Y + d / 2)
        ph = sign * (np.angle(Zp) - np.angle(Zm))
        return (np.tanh(np.abs(Zp) / XI) * np.tanh(np.abs(Zm) / XI)
                * np.exp(1j * ph))

    return (pair(-s, +1) * pair(+s, -1)).astype(complex)


def hopf_link_3d(n, dx, R, z1=-2.0):
    """Two LINKED vortex rings (Hopf link): ring 1 with axis z, centre
    (0,0,z1); ring 2 with axis x, centre (0, R-1, z1), threading ring 1.
    Each ring is a 2D vortex-image pair in its own (s, axial) half-plane
    (closed rings: periodic-clean, no net flux through any face).

    GEOMETRY NOTE (recorded per rule 1): the pre-registration wrote
    'two oblique colliding vortex rings'. That geometry was built and
    validated first -- and the rings passed through each other without
    reconnecting (probe run: final probe-plane punctures = the initial
    rings, intact, at the post-crossing positions; the echo recovered
    them at F = 0.9999). A topology-FORCED geometry replaces it: linked
    rings cannot separate without reconnecting, and the first
    reconnection merges them 2 -> 1 (clean discriminator). Event class
    and decision rules unchanged."""
    x = (np.arange(n) - n / 2) * dx
    X, Y, Z = np.meshgrid(x, x, x, indexing="ij")
    cy = R - 1.0

    # ring 1: axis z, centre (0, 0, z1)
    s1 = np.sqrt(X**2 + Y**2)
    chi1 = np.arctan2(Z - z1, s1 - R) - np.arctan2(Z - z1, s1 + R)
    amp1 = np.tanh(np.sqrt((s1 - R)**2 + (Z - z1)**2) / XI)
    # ring 2: axis x, centre (0, cy, z1)
    s2 = np.sqrt((Y - cy)**2 + (Z - z1)**2)
    chi2 = np.arctan2(X, s2 - R) - np.arctan2(X, s2 + R)
    amp2 = np.tanh(np.sqrt((s2 - R)**2 + X**2) / XI)
    return (amp1 * amp2 * np.exp(1j * (chi1 + chi2))).astype(complex)


def low_density_components(psi, thr=0.25, min_size=5):
    """Connected low-density components above a minimum voxel count
    (diagnostic only -- a 2-ring -> 2-ring exchange reconnection does
    not change this count; the binding 3D observable is the probe-plane
    winding pattern below)."""
    from scipy.ndimage import label
    lab, nlab = label(np.abs(psi)**2 < thr)
    if nlab == 0:
        return 0
    sizes = np.bincount(lab.ravel())
    return int(np.sum(sizes[1:] >= min_size))


def plane_punctures(psi3d, dx, axis, index):
    """Probe-plane winding pattern (the pre-registered probe-loop
    observable): plaquette windings of the field restricted to one
    coordinate plane. A vortex line puncturing the plane appears as a
    +-1 winding at the puncture point; ring reconnection rearranges the
    puncture pattern even when the number of rings is unchanged."""
    sl = [slice(None)] * 3
    sl[axis] = index
    return defects_2d(psi3d[tuple(sl)], dx)


